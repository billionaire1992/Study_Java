public class Operator04 {
	public static void main(String[] args) {
		//비교연산자 ▶ 조건문에 많이 사용 : 연산의 결과는 true, false 반환 → boolean Type
		//대소관계	: > , >=, <, <= ▶ 수학식에서 사용하는 연산자와 동일
		//동등관계	: == (같다), != (같지 않다)
				
		int num1 = 5, num2 =10;		//선언, 할당, 초기화 , 나열
		
		System.out.println(num1 >  num2);	
		System.out.println(num1 >= num2);
		System.out.println(num1 <  num2);
		System.out.println(num1 <= num2);
		System.out.println(num1 == num2);
		System.out.println(num1 != num2);
		
		
		
	}//main()
}//class